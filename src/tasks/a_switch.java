package tasks;

public class a_switch {
//find char vowels
	public static void main(String[] args) {
		char vowels='e';
		switch (vowels) {
		case 'e':
		System.out.println(" e is vowels");
		break;
		case 'a': 
			System.out.println(" a is vowels");
			break;
		case 'i': 
			System.out.println(" i is vowels");
		break;
		case 'o': 
			System.out.println(" o is vowels");
			break;
		case 'u': 
			System.out.println(" u is vowels");
		default :
			System.out.println(" it is constrant");
			break;
		}
	}

}
